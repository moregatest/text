package detect

import (
	"bytes"
	"fmt"
	"testing"
)

func TestEncoding(t *testing.T) {
	testCase := []struct {
		e   CharEncoding
		txt []byte
	}{
		{e: UTF8, txt: []byte("Hello World")},
		{e: UTF8, txt: []byte("こんにちは。世界の国から。")},
		{e: ShiftJIS, txt: []byte{0x82, 0xb1, 0x82, 0xf1, 0x82, 0xc9, 0x82, 0xbf, 0x82, 0xcd, 0x81, 0x42, 0x90, 0xa2, 0x8a, 0x45, 0x82, 0xcc, 0x8d, 0x91, 0x82, 0xa9, 0x82, 0xe7, 0x81, 0x42}},
		{e: EUCJP, txt: []byte{0xa4, 0xb3, 0xa4, 0xf3, 0xa4, 0xcb, 0xa4, 0xc1, 0xa4, 0xcf, 0xa1, 0xa3, 0xc0, 0xa4, 0xb3, 0xa6, 0xa4, 0xce, 0xb9, 0xf1, 0xa4, 0xab, 0xa4, 0xe9, 0xa1, 0xa3}},
		{e: ISO2022JP, txt: []byte{0x1b, 0x24, 0x42, 0x24, 0x33, 0x24, 0x73, 0x24, 0x4b, 0x24, 0x41, 0x24, 0x4f, 0x40, 0x24, 0x33, 0x26, 0x1b, 0x28, 0x42}},
	}

	for _, tst := range testCase {
		e := EncodingJa(bytes.NewReader(tst.txt))
		if e != tst.e {
			t.Errorf("Encoding(%v)  = \"%v\", want \"%v\".", tst.txt, e, tst.e)
		}
	}
}

